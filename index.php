<div style="font-size: 250%; color: green; text-align: center;">What is Masymanwiki?</div>
<div style="font-size:150%; text-align:center;"><b>Masymanwiki</b> is free online guidebook. It is Powered by <a href="https://maamedia.org">Maamedia</a> from Indonesia and Finland. How to... guidebook is created from quality instructions on how things work, how things are used, why someone exists. Masymanwiki strives to be a guidebook for everyone so that people learn to do things right and succeed. Masymanwiki is free to use, read, share, browse and edit. It is edited and maintained by volunteers in the same way as Maamedia. The information on Masymanwiki is reliable and the articles contain sources and references. This allows the reader to check the right of information if they wish. Masymanwiki is currently written in 4 different languages.<br><br>Masymanwiki is open to anyone living on Earth. <br>All people should have the right to use it in all ways at all times. System administrators are responsible for the availability, infrastructure, technology, maintenance and upgrades of Masymanwiki, and Masymanwiki is always ready to run smoothly and quickly.</div>
